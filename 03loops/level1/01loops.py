# print 1 to 10 numbers in for loop

# in python For loops : first of all in python for loop are iterator based ,not index based loop

for i in range(1,11):
    print(i)

#  why i write range(1,11) bcz 1 stand for the starting value and 11 stand for the ending value but in output we see printing value from 1 to 10  
#  this is bcz Python range() uses a half-open interval , where the starting value is included but the stop value is excluded 